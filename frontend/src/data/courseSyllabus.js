function normalizeCourseKey(course) {
  const value = `${course?.slug || ""} ${course?.title || ""}`.toLowerCase();

  if (value.includes("python")) return "python-programming";
  if (value.includes("web")) return "web-development";

  if (
    value.includes("mysql") ||
    value.includes("sql") ||
    value.includes("database")
  ) {
    return "mysql";
  }

  if (value.includes("c programming")) return "c-programming";

  if (
    value.includes("oops") ||
    value.includes("object-oriented") ||
    value.includes("object oriented")
  ) {
    return "oops";
  }

  if (
    value.includes("dsa") ||
    value.includes("data structure") ||
    value.includes("algorithm")
  ) {
    return "data-structures-algorithms";
  }

  return course?.slug;
}

export function enrichCourseWithSyllabus(course) {
  const key = normalizeCourseKey(course);
  const syllabus = courseSyllabus[key] || [];

  return {
    ...course,
    syllabus:
      Array.isArray(course?.syllabus) && course.syllabus.length > 0
        ? course.syllabus
        : syllabus,
  };
}

export function enrichCoursesWithSyllabus(courses = []) {
  return courses.map(enrichCourseWithSyllabus);
}
