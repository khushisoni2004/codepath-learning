export function getCourseBySlug(slug) {
  const current = String(slug || "").toLowerCase();
  return courseCatalog.find((course) => {
    const slugs = [course.slug, course.id, course.title, ...(course.aliases || [])]
      .filter(Boolean)
      .map((item) => String(item).toLowerCase().replace(/\s+/g, "-"));
    return slugs.includes(current);
  });
}
